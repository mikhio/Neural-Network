engict = {
    'A':'0',       'B':'1',       'C':'10',      'D':'11',     
    'E':'100',     'F':'101',     'G':'110',     'H':'111',    
    'I':'1000',    'J':'1001',    'K':'1010',    'L':'1011',   
    'M':'1100',    'N':'1101',    'O':'1110',    'P':'1111',   
    'Q':'10000',   'R':'10001',   'S':'10010',   'T':'10011',  
    'U':'10100',   'V':'10101',   'W':'10110',   'X':'10111',  
    'Y':'11000',   'Z':'11001',   'a':'11010',   'b':'11011',  
    'c':'11100',   'd':'11101',   'e':'11110',   'f':'11111',  
    'g':'100000',  'h':'100001',  'i':'100010',  'j':'100011', 
    'k':'100100',  'l':'100101',  'm':'100110',  'n':'100111', 
    'o':'101000',  'p':'101001',  'q':'101010',  'r':'101011', 
    's':'101100',  't':'101101',  'u':'101110',  'v':'101111', 
    'w':'110000',  'x':'110001',  'y':'110010',  'z':'110011'
}


def convToBin(word):
    resist = []
    for char in word:
        for let in engict:
            if let == char:
                for i in engict[let]:
                    resist.append(int(i))
                break

    if len(resist) < 126:
        resist.reverse()
        for i in range(126 - len(resist)):
            resist.append(0)
        resist.reverse()

    return resist


if __name__ == '__main__':
    str = input()
    print(convToBin(str))
